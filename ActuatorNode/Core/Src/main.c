  startEncoder();
  motorInit(&motor1, &htim1, TIM_CHANNEL_1, TIM_CHANNEL_4, L_EN_GPIO_Port, L_EN_Pin, R_EN_GPIO_Port, R_EN_Pin);
  startMotor(&motor1);
  initServo();
  printf("Actuator\n");
